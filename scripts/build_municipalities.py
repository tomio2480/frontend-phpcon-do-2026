def distribute_sapporo_furusato(municipalities: list[tuple]) -> list[tuple]:
    """札幌市のふるさと納税を10区に人口比で按分する。

    各区の按分後に合計が目標値と一致するよう、最後の区で端数を調整する。
    """
    sapporo_codes = {f"011{i:02d}" for i in range(1, 11)}
    sapporo_pops = {
        code: pop
        for code, _, _, _, _, pop, *_ in municipalities
        if code in sapporo_codes
    }
    total_pop = sum(sapporo_pops.values())
    if total_pop == 0:
        return municipalities

    distributed: list[tuple] = []
    accumulated_amount = 0
    accumulated_count = 0
    sapporo_entries = [m for m in municipalities if m[0] in sapporo_codes]

    for i, (code, name, display_name, region, area, pop, *_) in enumerate(sapporo_entries):
        ratio = pop / total_pop
        if i < len(sapporo_entries) - 1:
            amount = round(SAPPORO_FURUSATO_AMOUNT * ratio)
            count = round(SAPPORO_FURUSATO_COUNT * ratio)
        else:
            # 最後の区で端数を調整して合計を目標値に一致させる
            amount = SAPPORO_FURUSATO_AMOUNT - accumulated_amount
            count = SAPPORO_FURUSATO_COUNT - accumulated_count
        accumulated_amount += amount
        accumulated_count += count
        distributed.append((code, name, display_name, region, area, pop, amount, count))

    sapporo_map = {m[0]: m for m in distributed}
    return [sapporo_map.get(m[0], m) for m in municipalities]


def build() -> tuple[dict, dict]:
    """JSON 出力用の辞書を生成する。"""
    data = distribute_sapporo_furusato(MUNICIPALITIES)

    codes = [m[0] for m in data]
    seen: set[str] = set()
    duplicates: set[str] = set()
    for c in codes:
        if c in seen:
            duplicates.add(c)
        else:
            seen.add(c)
    if duplicates:
        raise ValueError(f"重複する市区町村コードが検出されました: {sorted(duplicates)}")

    municipalities_json: dict[str, dict] = {}
    for (code, name, display_name, region,
         area, population, furusato_amount, furusato_count) in data:
        municipalities_json[code] = {
            "code": code,
            "name": name,
            "display_name": display_name,
            "region": region,
            "area": area,
            "population": population,
            "furusato_amount": furusato_amount,
            "furusato_count": furusato_count,
        }

    vals = municipalities_json.values()
    # area は浮動小数点数のため math.fsum で丸め誤差を抑制する
    total_json = {
        "area": round(math.fsum(m["area"] for m in vals), 2),
        "population": sum(m["population"] for m in vals),
        "furusato_amount": sum(m["furusato_amount"] for m in vals),
        "furusato_count": sum(m["furusato_count"] for m in vals),
        "data_notes": {
            "area_source": "国土地理院「全国都道府県市区町村別面積調」令和5年(2023)10月1日現在",
            "population_source": "住民基本台帳に基づく人口 2024年1月1日現在",
            "furusato_source": "総務省「ふるさと納税に関する現況調査結果」令和6年度実施分",
            "retrieved_at": "2026-05-31",
        },
    }

    # 北海道市区町村数の整合性チェック（10区 + 34市 + 144町村 = 188）
    expected = 188
    if len(municipalities_json) != expected:
        raise ValueError(
            f"市区町村数が正しくありません（期待値: {expected}, 実際: {len(municipalities_json)}）"
        )

    return municipalities_json, total_json


def main() -> None:
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    municipalities_json, total_json = build()

    municipalities_path = OUTPUT_DIR / "municipalities.json"
    total_path = OUTPUT_DIR / "total.json"

    municipalities_path.write_text(
        json.dumps(municipalities_json, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    total_path.write_text(
        json.dumps(total_json, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )

    print(f"Generated: {municipalities_path}")
    print(f"  entries : {len(municipalities_json)}")
    print(f"Generated: {total_path}")
    print(f"  total area      : {total_json['area']:,.2f} km2")
    print(f"  total population: {total_json['population']:,}")
    print(f"  total furusato  : {total_json['furusato_amount']:,} yen")
    print(f"  total count     : {total_json['furusato_count']:,}件")


if __name__ == "__main__":
    main()
