# 北海道の公式市区町村数（35市 + 129町 + 15村 = 179、札幌10区は1市としてカウント）
# 本スクリプトでは札幌を10区として展開するため 188 エントリ＋北方領土6村で計194エントリ
_OFFICIAL_MUNICIPALITY_COUNT = 179
_SAPPORO_WARD_CODES = frozenset(f"011{i:02d}" for i in range(1, 11))
_HOPPO_CODES = frozenset({"01695", "01696", "01697", "01698", "01699", "01700"})
_EXPECTED_ENTRY_COUNT = _OFFICIAL_MUNICIPALITY_COUNT - 1 + len(_SAPPORO_WARD_CODES) + len(_HOPPO_CODES)  # 194


def build() -> tuple[dict, dict]:
    """JSON 出力用の辞書を生成する。"""
    codes = [m[0] for m in MUNICIPALITIES]

    seen: set[str] = set()
    duplicates: set[str] = set()
    for c in codes:
        if c in seen:
            duplicates.add(c)
        else:
            seen.add(c)
    if duplicates:
        raise ValueError(f"重複する市区町村コードが検出されました: {sorted(duplicates)}")

    missing_wards = _SAPPORO_WARD_CODES - seen
    if missing_wards:
        raise ValueError(f"札幌市の区コードが不足しています: {sorted(missing_wards)}")

    if len(MUNICIPALITIES) != _EXPECTED_ENTRY_COUNT:
        raise ValueError(
            f"市区町村数が正しくありません"
            f"（期待値: {_EXPECTED_ENTRY_COUNT}, 実際: {len(MUNICIPALITIES)}）"
        )

    population = etl_population.load()
    furusato = etl_furusato.load(population=population)
    area = etl_area.load()

    municipalities_json: dict[str, dict] = {}
    for code, name, display_name, region in MUNICIPALITIES:
        if code in _HOPPO_CODES:
            # 北方領土は人口・ふるさと納税データが存在しないためゼロで補完する
            municipalities_json[code] = {
                "code": code,
                "name": name,
                "display_name": display_name,
                "region": region,
                "area": area[code],
                "population": 0,
                "furusato_amount": 0,
                "furusato_count": 0,
            }
        else:
            municipalities_json[code] = {
                "code": code,
                "name": name,
                "display_name": display_name,
                "region": region,
                "area": area[code],
                "population": population[code],
                "furusato_amount": furusato[code][0],
                "furusato_count": furusato[code][1],
            }

    vals = municipalities_json.values()
    total_json = {
        "area": round(math.fsum(m["area"] for m in vals), 2),
        "population": sum(m["population"] for m in vals),
        "furusato_amount": sum(m["furusato_amount"] for m in vals),
        "furusato_count": sum(m["furusato_count"] for m in vals),
        "data_notes": {
            "area_source": "国土数値情報 行政区域データ(N03,2024) のジオメトリから計算（近似値）",
            "population_source": "総務省 住民基本台帳に基づく人口 2024年1月1日現在",
            "furusato_source": "総務省「ふるさと納税に関する現況調査結果」令和7年度実施（令和6年度実績）",
            "retrieved_at": "2026-05-31",
        },
    }

    return municipalities_json, total_json


def main() -> None:
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    municipalities_json, total_json = build()

    municipalities_path = OUTPUT_DIR / "municipalities.json"
    total_path = OUTPUT_DIR / "total.json"

    municipalities_path.write_text(
        json.dumps(municipalities_json, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )
    total_path.write_text(
        json.dumps(total_json, ensure_ascii=False, indent=2) + "\n",
        encoding="utf-8",
    )

    print(f"Generated: {municipalities_path}")
    print(f"  entries : {len(municipalities_json)}")
    print(f"Generated: {total_path}")
    print(f"  total area      : {total_json['area']:,.2f} km2")
    print(f"  total population: {total_json['population']:,}")
    print(f"  total furusato  : {total_json['furusato_amount']:,} yen")
    print(f"  total count     : {total_json['furusato_count']:,}件")


if __name__ == "__main__":
    main()
